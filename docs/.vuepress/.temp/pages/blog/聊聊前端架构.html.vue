<h2 id="前端架构" tabindex="-1"><a class="header-anchor" href="#前端架构" aria-hidden="true">#</a> 前端架构</h2>
<p>首先明确一下业务、架构、工程化三者之间的关系：业务作为核心出发点，架构聚焦于代码，工程化聚焦于流程。</p>
<p>像上面所说的， 随着前端技术的发展，出现了React、Vue等，使得前端脱离的JSP、Velocity等服务端模板引擎，而独立发展， 前端能够承载更复杂的交互和业务逻辑。</p>
<h3 id="什么是前端架构" tabindex="-1"><a class="header-anchor" href="#什么是前端架构" aria-hidden="true">#</a> 什么是前端架构</h3>
<p>什么是架构，在《大型网站技术架构——核心原理与案例分析》一书中是这么描述的：架构是一种高层次的规划，决定产品的走向和最终蓝图。在前端架构中则是从代码层面考虑源码组织规范、组件化/模块化、数据管理、性能优化。</p>
<h3 id="什么是前端工程化" tabindex="-1"><a class="header-anchor" href="#什么是前端工程化" aria-hidden="true">#</a> 什么是前端工程化</h3>
<p>&quot;软件工程&quot;最初是为了解决软件危机，包含下面几个问题：</p>
<ul>
<li>代码不规范，维护成本高。</li>
<li>开发过程混乱，且难以管理</li>
<li>性能不理想，功能不稳定。</li>
<li>降低开发成本，减少冗余工作。</li>
</ul>
<p>代码和流程和软件工程的核心关注点。从代码的角度考虑，工程的目标是保证软件的高可用性、可扩展性、可伸缩行、性能和安全。在架构之外，工程从更宏观的角度完善开发和维护流程的管控，强调项目迭代的规范性、有序性、可控性和高效性。</p>
<p>前端工程体系是一种服务，以项目迭代过程中的前端开发为主要服务对象，涉及开 发、构建、部署等环节。对语言内在功能特性的补充，或在对其外在组织能力的补充。这些种种补充，尽是 在工程体系的“工具”这一隅上做的功夫。</p>
<center>前端工程化 = 前端技术架构 + 前端工程服务体系</center>
<h3 id="架构师的职责" tabindex="-1"><a class="header-anchor" href="#架构师的职责" aria-hidden="true">#</a> 架构师的职责</h3>
<p>前端架构师简单概况为两方面：</p>
<ul>
<li>根据业务特征设计合理的前端技术架构。</li>
<li>根据架构特征搭建合理的前端工程服务体系。</li>
</ul>
<p>在热力学里边有个概念“熵”，它用来衡量系统的混乱和无序程度。热力学第二定律描述：无外界干预的孤立系统会自动朝着热力学平衡——最大熵状态——演化，即变的越来越混乱。同样的，软件开发过程中也是随着需求的不断增加，整个项目会朝着无序化演进，冗余度越来越高，最终变的难以维护，变成一坨“屎山”。架构师便是通过及时，有效的干预，即在开发过程中不断进行微小的重构，以保持项目的有序。</p>
<h3 id="架构师在项目中的角色" tabindex="-1"><a class="header-anchor" href="#架构师在项目中的角色" aria-hidden="true">#</a> 架构师在项目中的角色</h3>
<p>架构师在开发团队中的角色有些特殊，除了架构设计、软件开发等技术工作外，可能还会承担一部分规划产品路线、安排人员职责分工、指导工程师工作、过程风险评估等其他工作，因为这些工作通常需要对产品技术架构、功能模块划分、技术风险都熟悉的架构师直接参与或负责。在开发过程中，还需要完成项目组内外的沟通协调工作。</p>
<h4 id="共同参与产品" tabindex="-1"><a class="header-anchor" href="#共同参与产品" aria-hidden="true">#</a> 共同参与产品</h4>
<p>所谓“一群优秀的人做一件共同热爱的事，那他们一定会成功”。好的项目管理不仅仅是制定计划、组织资源、跟踪和修正项目进度，更重要的是让项目组中的每一个成员都能理解产品，理解目标本身。架构师和项目组成员应该有一个共同描述的蓝图。</p>
<ul>
<li>产品要做什么，不做什么，要达到什么样的业务目标。</li>
<li>产品能为用户创造什么样的价值，实现什么样的市场目标，产品最终长什么样子等。</li>
</ul>
